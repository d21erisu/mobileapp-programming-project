package com.example.project;

public class Animal {

    String id;
    String name;
    String type;
    String company;
    String location;
    String category;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getCompany() {
        return company;
    }

    public String getLocation() {
        return location;
    }

    public String getCategory() {
        return category;
    }

}

